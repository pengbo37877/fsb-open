      custom_js: ""
    };
  },
  computed: {
    ...mapState({
      user: state => state.anti.user,
      shop: state => state.anti.shop,
      custom_code: state => state.anti.custom_code
    })
  },
  mounted() {
    console.log("Advanced mounted.");
    if (Object.keys(this.custom_code).length == 0) {
      this.getCustomCodes();
    } else {
      this.custom_css = this.custom_code.custom_css;
      this.custom_js = this.custom_code.custom_js;
      this.getCustomCodes();
    }
  },
  methods: {
    getCustomCodes() {
      axios.get("/spa/custom_codes").then(res => {
        this.$store.commit("anti/SET_CUSTOM_CODE", { custom_code: res.data });
        this.custom_css = res.data.custom_css;
        this.custom_js = res.data.custom_js;
      });
    },
    saveCustomJs() {
      axios
        .put("/spa/custom_js", {
          custom_js: this.custom_js
        })
        .then(res => {
          console.log(res.data);
          this.$store.commit("anti/SET_CUSTOM_CODE", { custom_code: res.data });
        });
    },
    saveCustomCss() {
      axios
        .put("/spa/custom_css", {
          custom_css: this.custom_css
        })
        .then(res => {
          console.log(res.data);

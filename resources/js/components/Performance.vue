import { BarChart } from "echarts/charts";
import {
  GridComponent,
  TitleComponent,
  TooltipComponent,
  LegendComponent
} from "echarts/components";
import VChart, { THEME_KEY } from "vue-echarts";

use([
  CanvasRenderer,
  BarChart,
  GridComponent,
  TitleComponent,
  TooltipComponent,
  LegendComponent
]);

export default {
  components: { DatePicker, VChart },
  data() {
    return {
      session_range: [],
      clicks_range: [],
      option: {
        title: {
          text: ""
        },
        tooltip: {},
        legend: {
          data: ["clicks"]
        },
        xAxis: {
          data: []
        },
        yAxis: {},
        series: [
          {
            name: "clicks",
            type: "bar",
            data: []
          }
        ]
      }
    };
  },
  computed: {
    ...mapState({
      user: state => state.anti.user,
      shop: state => state.anti.shop,
      currencies: state => state.anti.currencies,
      sessions: state => state.anti.sessions,
      clicks: state => state.anti.clicks,
      statistics: state => state.anti.statistics
    })
  },
  mounted() {
    var today = new Date();
    this.session_range.unshift(today.getTime());
    this.clicks_range.unshift(today.getTime());
    today.setDate(today.getDate() - 7);
    this.session_range.unshift(today.getTime());
    this.clicks_range.unshift(today.getTime());

    if (this.clicks.length > 0) {
      this.option.xAxis.data = [];
      this.option.series[0].data = [];
      this.clicks.forEach(click => {
        this.option.xAxis.data.push(click.day);
        this.option.series[0].data.push(parseInt(click.cc));
      });
    }

    this.getCurrencies();
    this.getStatistics();
    this.getSessions();
    this.getClicks();
  },
  methods: {
    getCurrencyImage(currency) {
      var c = this.currencies.find(c => c.currency === currency);
      if (c) {
        return c.image;
      }
      console.log("not found currency:" + currency);
    },
    getCurrencies() {
      axios.get("/spa/currencies").then(res => {
        this.$store.commit("anti/SET_CURRENCIES", { currencies: res.data });
      });
    },
    getStatistics() {
      console.log("getStatistics");
      axios.get("/spa/statistics").then(res => {
        console.log(res.data);
        this.$store.commit("anti/SET_STATISTICS", {
          statistics: res.data
        });
      });
    },
    getSessions() {
      console.log("getSessions");
      axios
        .get(
          "/spa/sessions?start=" +
            this.session_range[0] +
            "&end=" +
            this.session_range[1]
        )
        .then(res => {
          console.log(res.data);
          this.$store.commit("anti/SET_SESSIONS", {
            sessions: res.data
          });
        });
    },
    getClicks() {
      console.log("getClicks");
      axios
        .get(
          "/spa/clicks?start=" +
            this.clicks_range[0] +
            "&end=" +
            this.clicks_range[1]
        )
        .then(res => {
          console.log(res.data);
          // 处理数据
          this.$store.commit("anti/SET_CLICKS", { clicks: res.data });
          var clicks = res.data;
          this.option.xAxis.data = [];
          this.option.series[0].data = [];
          clicks.forEach(click => {
            this.option.xAxis.data.push(click.day);
            this.option.series[0].data.push(parseInt(click.cc));
          });
        });
    },

    gotoCCA() {
      window
        .open("https://apps.shopify.com/currency-converter-ant", "_blank")
        .focus();
    }
  }
};

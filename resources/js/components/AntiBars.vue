<script>
import axios from "axios";
import { mapState } from "vuex";
import BarItem from "./BarItem.vue";
export default {
  components: {
    BarItem
  },
  data() {
    return {
      fsbs: [],
      internal: null,
      tempCookie: ""
    };
  },
  computed: {
    ...mapState({
      user: state => state.anti.user,
      shop: state => state.anti.shop,
      shippingBars: state => state.anti.shippingBars
    })
  },
  mounted() {
    if (this.shippingBars.length > 0) {
      this.fsbs = this.shippingBars;
    }
    this.internal = setInterval(() => {
      if (this.fsbs.length === 0) {
        this.getFsbs();
        this.getShop();
        this.getUser();
        this.getCustomCodes();
        this.getCurrencies();
      } else {
        clearInterval(this.internal);
      }
    }, 500);
    // back load
    this.getFsbs();
  },
  methods: {
    getFsbs() {
      axios.get("/spa/fsbs").then(res => {
        this.fsbs = res.data;
        this.$store.commit("anti/SET_SHIPPING_BARS", {
          shippingBars: res.data
        });
      });
    },
    getUser() {
      axios.get("/spa/user").then(res => {
        this.$store.commit("anti/SET_USER", { user: res.data });
      });
    },
    getShop() {
      axios.get("/spa/shop").then(res => {
        this.$store.commit("anti/SET_SHOP", { shop: res.data });
      });
    },
    getCustomCodes() {
      axios.get("/spa/custom_codes").then(res => {
        this.$store.commit("anti/SET_CUSTOM_CODE", { custom_code: res.data });
      });
    },
    getCurrencies() {
      axios.get("/spa/currencies").then(res => {
        this.$store.commit("anti/SET_CURRENCIES", { currencies: res.data });
      });
    },
    gotoCCA() {
      window
        .open("https://apps.shopify.com/currency-converter-ant", "_blank")
        .focus();
    }
  }
};
</script>
